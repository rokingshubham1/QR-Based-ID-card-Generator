    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Connection con = DBConnection.getConnection();
        PreparedStatement ps;
        
        if(jRadioButton1.isSelected())
            {
                gen="Male";
            }
        if(jRadioButton2.isSelected())
            {
                gen="Female"; }
        try{
            ps = con.prepareStatement("INSERT INTO `qrdata`.`generatecode` "
                    + "(`Fname`, `LName`, `Gender`, `DOB`, `Address`, `Pic`) "
                    + "VALUES (?,?,?,?,?,?);");
            ps.setString(1,jTextField1.getText());
            ps.setString(2,jTextField2.getText());
            ps.setString(3,gen);
            ps.setString(4,jFormattedTextField1.getText());
            ps.setString(5,jTextField3.getText());
            
            InputStream img=new FileInputStream(new File(imgPath));
            ps.setBlob(6,img);
            if(ps.executeUpdate() !=0)
            {
                JOptionPane.showMessageDialog(null,"Account Created");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Something Wrong");
            }
            
        } catch (SQLException ex) {
             Logger.getLogger(CardGenerator.class.getName()).log(Level.SEVERE, null, ex);
         }  catch (FileNotFoundException ex) {
                Logger.getLogger(CardGenerator.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        //DynamicQr.generate_qr(jTextField1,jTextField3);
        
        jTextArea1.setText("Here is Your Id Card Generated");
        jTextArea1.setText(jTextArea1.getText()+ "Get Your Qr Code" + jTextField1.getText());
        
        jTextArea1.setText(jTextArea1.getText()+ " Scan to get more information ");
        jTextArea1.setText(jTextArea1.getText()+ "Name" + jTextField2.getText());
        jTextArea1.setText(jTextArea1.getText()+ "Date Of Birth :" + jFormattedTextField1.getText());
        
    }//GEN-LAST:event_jButton3ActionPerformed

  
