    public ImageIcon resizePic(String picPath)
    {
        ImageIcon myImg = new ImageIcon(picPath);
        Image img= myImg.getImage().getScaledInstance(jLabelPic.getWidth(),jLabelPic.getHeight(), jLabelPic.getWidth());
        ImageIcon myPicture = new ImageIcon(img);
        return myPicture;
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser fchoser=new JFileChooser();
        fchoser.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter fileFilter= new FileNameExtensionFilter("*.Images","jpg","png","jpeg");
        fchoser.addChoosableFileFilter(fileFilter);
        int fileState=fchoser.showSaveDialog(null);
        if(fileState == JFileChooser.APPROVE_OPTION)
        {
            File selectedFile=fchoser.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            imgPath=path;
            jLabelPic.setIcon(resizePic(path));
            //jLabelPic.setIcon(new ImageIcon(path));
        }
        else if(fileState==JFileChooser.CANCEL_OPTION)
        {
            System.out.println("No Image Selected");
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

